package mello.sergio;

public class Calculator {

    public static double sum(double numberX, double numberY) {
        return numberX + numberY;
    }

    public static double subtraction(double numberX, double numberY) {
        return numberX - numberY;
    }

    public static double multiplication(double numberX, double numberY) {
        return numberX * numberY;
    }

    public static double division(double numberX, double numberY) {
        return numberX / numberY;
    }

}
